# ─── Лист-легенда ─────────────────────────────────────────────────────────────
def sheet_legend(wb: Workbook):
    ws = wb.create_sheet("Легенда", 0)   # первый лист
    ws.column_dimensions["A"].width = 22
    ws.column_dimensions["B"].width = 48

    title_font = Font(name="Calibri", bold=True, size=14)
    bold_font  = Font(name="Calibri", bold=True, size=11)
    norm_font  = Font(name="Calibri", size=11)

    def row(r, a, b, bold=False, fill_hex=None):
        ca = ws.cell(row=r, column=1, value=a)
        cb = ws.cell(row=r, column=2, value=b)
        f = bold_font if bold else norm_font
        ca.font = f; cb.font = f
        ca.alignment = left(); cb.alignment = left()
        if fill_hex:
            ca.fill = PatternFill("solid", fgColor=fill_hex)
            cb.fill = PatternFill("solid", fgColor=fill_hex)
        ca.border = thin_border(); cb.border = thin_border()

    ws.cell(row=1, column=1, value="TEST-CASE: Shokoladnitsa VK Mini App").font = title_font
    ws.merge_cells("A1:B1")
    ws.row_dimensions[1].height = 30

    row(3, "Дата создания", "2026-03-07")
    row(4, "Версия проекта", "VK-Integration branch")
    row(5, "Стек", "FastAPI + SQLAlchemy + PostgreSQL + React + VKUI v7")
    row(6, "Автор", "Shokoladnitsa Team")

    ws.cell(row=8, column=1, value="Листы").font = bold_font
    ws.cell(row=8, column=2, value="Описание").font = bold_font

    sheets_info = [
        ("Frontend — Форма", "Тесты формы бронирования (VK Mini App, React/VKUI)"),
        ("Backend — API",    "Тесты HTTP API эндпоинтов (FastAPI)"),
        ("VK Bot",           "Тесты VK Long Poll бота (команды, уведомления, CSV, графики)"),
        ("База данных",      "Тесты моделей, constraint'ов и миграций (PostgreSQL / SQLAlchemy)"),
        ("Планировщик",      "Тесты scheduler.py (задачи: confirmation, reminder, feedback)"),
        ("Безопасность",     "Тесты безопасности: SQLi, XSS, CORS, rate limit, headers"),
        ("Автотесты (pytest)","Тест-кейсы запуска pytest-набора (backend/tests/)"),
    ]
    for i, (name, desc) in enumerate(sheets_info, 9):
        row(i, name, desc)

    ws.cell(row=17, column=1, value="Статусы").font = bold_font
    statuses = [
        ("Не проверен", "Тест ещё не выполнен", None),
        ("Пройдено",    "Тест прошёл успешно",   COLOR_PASS),
        ("Провалено",   "Тест не прошёл",        COLOR_FAIL),
        ("Заблокировано","Тест невозможно выполнить", COLOR_BLOCK),
    ]
    for i, (s, d, c) in enumerate(statuses, 18):
        row(i, s, d, fill_hex=c)

    ws.cell(row=23, column=1, value="Приоритеты").font = bold_font
    priorities = [
        ("Критический", "Блокирует продакшн, требует немедленного исправления"),
        ("Высокий",     "Основная функциональность, тестируется в первую очередь"),
        ("Средний",     "Важно, но не блокирует основной поток"),
        ("Низкий",      "Nice-to-have, тестируется в последнюю очередь"),
    ]
    for i, (p, d) in enumerate(priorities, 24):
        row(i, p, d)


# ─── Главная функция ──────────────────────────────────────────────────────────
def main():
    wb = Workbook()
    # Удалить стандартный пустой лист
    if "Sheet" in wb.sheetnames:
        del wb["Sheet"]

    sheet_legend(wb)
    sheet_frontend(wb)
    sheet_api(wb)
    sheet_vkbot(wb)
    sheet_database(wb)
    sheet_scheduler(wb)
    sheet_security(wb)
    sheet_autotests(wb)

    out = "testcases.xlsx"
    wb.save(out)
    print(f"OK: {out}")
    print(f"   Sheets: {len(wb.sheetnames)}")
    for s in wb.sheetnames:
        print(f"   - {s}")


if __name__ == "__main__":
    main()
