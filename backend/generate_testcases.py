# ─── Цвета ───────────────────────────────────────────────────────────────────
COLOR_HEADER_BG   = "4472C4"   # синий — заголовок таблицы
COLOR_HEADER_FONT = "FFFFFF"   # белый текст
COLOR_PASS        = "C6EFCE"   # зелёный — Пройдено
COLOR_FAIL        = "FFC7CE"   # красный — Провалено
COLOR_BLOCK       = "FFEB9C"   # жёлтый — Заблокировано
COLOR_SECTION_BG  = "D9E1F2"   # светло-синий — разделитель секции
COLOR_ALT_ROW     = "F2F2F2"   # серый — чётные строки

# ─── Стили ───────────────────────────────────────────────────────────────────
def header_font():
    return Font(name="Calibri", bold=True, color=COLOR_HEADER_FONT, size=11)

def header_fill():
    return PatternFill("solid", fgColor=COLOR_HEADER_BG)

def section_fill():
    return PatternFill("solid", fgColor=COLOR_SECTION_BG)

def alt_fill():
    return PatternFill("solid", fgColor=COLOR_ALT_ROW)

def center():
    return Alignment(horizontal="center", vertical="center", wrap_text=True)

def left():
    return Alignment(horizontal="left", vertical="center", wrap_text=True)

def thin_border():
    s = Side(style="thin")
    return Border(left=s, right=s, top=s, bottom=s)


def write_headers(ws, headers: list[str], col_widths: list[int]):
    """Пишет строку заголовков и задаёт ширину колонок."""
    for col, (h, w) in enumerate(zip(headers, col_widths), 1):
        cell = ws.cell(row=1, column=col, value=h)
        cell.font = header_font()
        cell.fill = header_fill()
        cell.alignment = center()
        cell.border = thin_border()
        ws.column_dimensions[get_column_letter(col)].width = w
    ws.row_dimensions[1].height = 28


def write_section(ws, row: int, title: str, ncols: int):
    """Пишет строку-разделитель секции (merged, синий фон)."""
    ws.merge_cells(start_row=row, start_column=1,
                   end_row=row, end_column=ncols)
    cell = ws.cell(row=row, column=1, value=title)
    cell.font = Font(name="Calibri", bold=True, size=11)
    cell.fill = section_fill()
    cell.alignment = left()
    cell.border = thin_border()
    ws.row_dimensions[row].height = 20


def write_row(ws, row: int, values: list, is_alt: bool = False):
    """Пишет строку данных."""
    for col, v in enumerate(values, 1):
        cell = ws.cell(row=row, column=col, value=v)
        cell.alignment = left()
        cell.border = thin_border()
        if is_alt:
            cell.fill = alt_fill()
        # ID-колонка по центру
        if col == 1:
            cell.alignment = center()


